// Aritmeticos
    15 + 10 // 25 (+) SUMA
    10 - 15 // -5 (-) RESTA
    10 * 15 // 150 (*) MULTIPLICACION
    15 / 10 // 1.5 (/) DIVISION

// Otros aritmeticos
    15++ // Incremento, es igual a 15+1.
    15-- // Decremento, es igual a 15-1.

// MODULO (Resto de una division)
    15 % 5 // Es el resto de dividir 15 / 5, es igual a 0.
    15 % 2 // Es el resto de dividir 15 / 2, es igual a 1.

// Comparacion SIMPLE - compara dos valores y devuelve true o false
    10 == 15 // Igualdad, false.
    10 != 15 // Desigualdad, true.

// Comparacion ESTRICTA - compara dos valores pero tambien es tipo de dato, tambien devuelve true o false
    10 === "10" // Igualdad estricta - false.
    10 !== 15 // Desigualdad estricta - true.

//Continuacion de comparacion - compara dos valores y devuelve true o false
    15 > 15 // Mayor que, false.
    15 >= 15 // Mayor o igual que, true.
    10 < 15 // Menor que, true.
    10 <= 15 // Menor o igual que, true.

//Logicos - permiten combinar valores booleanos, y el resultado tambien devuelve un booleano
    (20 > 15) && (10 != 25) // AND (&&) - ambas expresiones deben dar true para que el resultado final de true.
    (20 > 15) || (10 != 25) // OR (||) - basta que una expresion de true, para que el resultado final sean true.
// (!) NOT
    !true // false
    !(10 > 15) // true

